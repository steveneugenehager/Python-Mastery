#In Python, a variable is a name that refers to a value stored in memory. 
# It is used to store data such as numbers, text, lists, or other objects 
# so you can easily access and manipulate that data throughout your program.
#
# Variables are set using the assingment operator, '='.
# Variables names 
# - cannot start with a number
# - must start with a letter or underscore
# - can contain numbers, letters, or the underscore.
# - capitalization matters

# The preferred style for variable (and function) names in Python is lowercase with words separated by underscores 
# (known as snake_case). This convention is part of Python's PEP 8 style guide.
my_variable_name = 'a' # Preferred Convention for Python

# Names with double underscores are reserved for special methods and attributes in Python.

# for Constants:  Use uppercase with underscores.
#   Example: 
MAX_SIZE = 80

# Lower camel case: The first letter is lowercase, and subsequent words start with uppercase.
myVariableName  = 0  # Not the preferred convention...

# Multiple variables can be assigned at the same time using a tuple on both sides of the assignment operator:

x , y , z = 30 , -60 , 180
print (" x , y , z = " ,x, y ,z )

